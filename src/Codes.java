import java.util.List;

public class Codes {
	private List<String> codes = null;
	
	/**
	*
	* @param codes
	*/
	public Codes(List<String> codes) {
		super();
		this.codes = codes;
	}
	
	public List<String> getCodes() {
		return codes;
	}
	
	public void setCodes(List<String> codes) {
		this.codes = codes;
	}
}